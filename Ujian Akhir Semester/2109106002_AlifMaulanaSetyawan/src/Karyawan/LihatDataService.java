    public void ShowData(){
        String loggedInUsername = session.getLoggedInUsername();
        
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        
        try{
            conn = Koneksi.getConnection();
            stmt = conn.createStatement();
            
            // Query Lihat
            String sql = "SELECT * FROM DataService WHERE Username = '%s'";
            sql = String.format(sql, loggedInUsername); 

            // Eksekusi
            rs = stmt.executeQuery(sql);
            while(rs.next()){
                Object[] o = new Object[9];
                o[0] = rs.getInt("IdService");
                o[1] = rs.getString("TanggalService");
                o[2] = rs.getString("NamaCust");
                o[3] = rs.getString("NoHpCust");
                o[4] = rs.getString("MerekHp");
                o[5] = rs.getString("Diagnosis");
                o[6] = rs.getString("NamaTeknisi");
                o[7] = rs.getString("KeahlianTeknisi");
                o[8] = rs.getString("NoHpTeknisi");
                model.addRow(o);
            }
            rs.close();
            stmt.close();
            
        }catch (Exception Error){
            System.out.println("" + Error);
        }
        
    }

