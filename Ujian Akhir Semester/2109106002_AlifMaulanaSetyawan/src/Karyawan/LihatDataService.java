    private void btnEditMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEditMouseClicked
        int i = tabelservice.getSelectedRow();
        if (i == -1){
            JOptionPane.showMessageDialog(this, "Pilih Data Pada Tabel.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        int ID = Integer.parseInt(model.getValueAt(i, 0).toString());
        
        FormEditService edit = new FormEditService();
        edit.setID(ID);
        
        String Tanggal, Nama, NoHp, MerekHp, Diagnosis, IdTeknisi;
        
        try {
            Tanggal = (String) model.getValueAt(i, 1);

            // Ubah tanggal dari tipe String menjadi tipe Date
            SimpleDateFormat dateFormat = new SimpleDateFormat("dd-MM-yyyy");
            Date tanggal = dateFormat.parse(Tanggal);

            // Set nilai tanggal pada JDateChooser
            edit.dtpTanggalEdit.setDate(tanggal);
        } catch (Exception Error){
            System.out.println("" + Error);
        }
        
        Nama = (String) model.getValueAt(i, 2);
        
        edit.txtNamaEdit.setText(Nama);
        
        
        NoHp = (String) model.getValueAt(i, 3);
        
        edit.txtNoHpEdit.setText(NoHp);
        
        MerekHp = (String) model.getValueAt(i, 4);
        
        edit.txtMerekHpEdit.setText(MerekHp);
        
        Diagnosis = (String) model.getValueAt(i, 5);
        
        edit.cbxDiagnosisEdit.setSelectedItem(Diagnosis);
        
        try {
            conn = Koneksi.getConnection();

            // Query SELECT
            String sql = "SELECT * FROM DataService WHERE IdService = ?";
            ps = conn.prepareStatement(sql);
            ps.setInt(1, ID);
            rs = ps.executeQuery();

            if (rs.next()) {
                // Ambil nilai dari hasil query
                int IDTeknisi = rs.getInt("IdTeknisi");
                String NamaTeknisi = rs.getString("Namateknisi");
                String KeahlianTeknisi = rs.getString("KeahlianTeknisi");
                String NoHpTeknisi = rs.getString("NoHpTeknisi");

                edit.cbxIdTeknisiEdit.setSelectedItem(String.valueOf(IDTeknisi));
                edit.txtNamaTeknisiEdit.setText(NamaTeknisi);
                edit.txtKeahlianTeknisiEdit.setText(KeahlianTeknisi);
                edit.txtNoHpTeknisiEdit.setText(NoHpTeknisi);
            }

            rs.close();
            ps.close();
        } catch (Exception Error) {
            System.out.println("" + Error);
        }
    
        setVisible(false);
        edit.setVisible(true);
    }//GEN-LAST:event_btnEditMouseClicked

