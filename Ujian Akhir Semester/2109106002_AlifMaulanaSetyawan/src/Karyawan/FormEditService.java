    private void cbxIdTeknisiEditFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_cbxIdTeknisiEditFocusLost
        try {
            conn = Koneksi.getConnection();
                        
            String idTeknisi = cbxIdTeknisiEdit.getSelectedItem().toString();
            String query = "SELECT * FROM DataTeknisi WHERE IdTeknisi = ?";
            ps = conn.prepareStatement(query);
            ps.setString(1, idTeknisi);
            rs = ps.executeQuery();

            if (rs.next()) {
                String namaTeknisi = rs.getString("Nama");
                String keahlian = rs.getString("Keahlian");
                String noHpTeknisi = rs.getString("NoHp");

                txtNamaTeknisiEdit.setText(namaTeknisi);
                txtKeahlianTeknisiEdit.setText(keahlian);
                txtNoHpTeknisiEdit.setText(noHpTeknisi);
            }
            
            rs.close();
            ps.close();
        } catch (Exception Error) {
            System.out.println("" + Error);
        }
    }//GEN-LAST:event_cbxIdTeknisiEditFocusLost

