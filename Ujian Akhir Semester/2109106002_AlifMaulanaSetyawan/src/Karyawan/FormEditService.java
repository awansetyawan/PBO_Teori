    private void btnSimpanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSimpanMouseClicked
        String Nama, NoHp, Merek, Diagnosis, NamaTeknisi, KeahlianTeknisi, NoHpTeknisi;
        int IDTeknisi;
        
        String tampilan = "dd-MM-yyyy";
        SimpleDateFormat fm = new SimpleDateFormat(tampilan);
        String tanggal = "";
        
        if (dtpTanggalEdit.getDate() != null) {
            tanggal = fm.format(dtpTanggalEdit.getDate());
        } else {
            JOptionPane.showMessageDialog(this, "Pilih Tanggal & Perhatikan Format.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
       
        Nama = txtNamaEdit.getText();
        NoHp = txtNoHpEdit.getText();
        Merek = txtMerekHpEdit.getText();
        Diagnosis = cbxDiagnosisEdit.getSelectedItem().toString();
        
        IDTeknisi = Integer.parseInt(cbxIdTeknisiEdit.getSelectedItem().toString());
        NamaTeknisi = txtNamaTeknisiEdit.getText();
        KeahlianTeknisi = txtKeahlianTeknisiEdit.getText();
        NoHpTeknisi = txtNoHpTeknisiEdit.getText();
        
        try {
            conn = Koneksi.getConnection();
            stmt = conn.createStatement();
            
            if (Nama.isEmpty()) {
                txtNamaEdit.requestFocus();
                JOptionPane.showMessageDialog(this, "Nama Tidak Boleh Kosong.", "Error", JOptionPane.ERROR_MESSAGE);
            }
            else if (NoHp.isEmpty()) {
                txtNoHpEdit.requestFocus();
                JOptionPane.showMessageDialog(this, "Nomor Hp Customer Tidak Boleh Kosong.", "Error", JOptionPane.ERROR_MESSAGE);
            }
            else if (Merek.isEmpty()) {
                txtMerekHpEdit.requestFocus();
                JOptionPane.showMessageDialog(this, "Merek Hp Tidak Boleh Kosong.", "Error", JOptionPane.ERROR_MESSAGE);
            }
            else if (Diagnosis.isEmpty()){
                cbxDiagnosisEdit.requestFocus();
                JOptionPane.showMessageDialog(this, "Diagnosis Tidak Boleh Kosong.", "Error", JOptionPane.ERROR_MESSAGE);
            }
            else if (IDTeknisi == 0){
                cbxIdTeknisiEdit.requestFocus();
                JOptionPane.showMessageDialog(this, "Id Teknisi Tidak Boleh Kosong.", "Error", JOptionPane.ERROR_MESSAGE);
            }
            else if ((txtNamaTeknisiEdit.getText()).isEmpty()){
                cbxIdTeknisiEdit.requestFocus();
                JOptionPane.showMessageDialog(this, "Nama, NoHp, & Keahlian Teknisi Tidak Boleh Kosong.", "Error", JOptionPane.ERROR_MESSAGE);
            }else{
                
                // Query Update
                String sql = "UPDATE DataService SET TanggalService = ?, NamaCust = ?, NoHpCust = ?, MerekHp = ?, Diagnosis = ?, IdTeknisi = ?, Namateknisi = ?, KeahlianTeknisi = ?, NoHpTeknisi = ? WHERE IdService = ?";
                PreparedStatement ps = conn.prepareStatement(sql);
                ps.setString(1, tanggal);
                ps.setString(2, Nama);
                ps.setString(3, NoHp);
                ps.setString(4, Merek);
                ps.setString(5, Diagnosis);
                ps.setInt(6, IDTeknisi);
                ps.setString(7, NamaTeknisi);
                ps.setString(8, KeahlianTeknisi);
                ps.setString(9, NoHpTeknisi);
                ps.setInt(10, getID());
                ps.executeUpdate();
                ps.close();

                JOptionPane.showMessageDialog(this, "Data Service Telah Berhasil Di Simpan!", "Informasi", JOptionPane.INFORMATION_MESSAGE); 

                setVisible(false);
        
                LihatDataService lihat = new LihatDataService();
                lihat.setVisible(true);
            }

        }catch (Exception Error) {
            System.out.println("" + Error);
        }
        
    }//GEN-LAST:event_btnSimpanMouseClicked

